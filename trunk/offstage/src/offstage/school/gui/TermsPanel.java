public void initRuntime(FrontApp xfapp, SchoolModel smod, SqlRunner str)
//throws SQLException
{
	this.fapp = xfapp;
	this.smod = smod;

	smod.addListener(new SchoolModel.Adapter() {
    public void termIDChanged(int oldTermID, int termID) {
			termChanged(fapp.getBatchSet());
	}});

	
	oneTermDm = new IntKeyedDbModel(fapp.getSchema("termids"),
		"groupid", fapp.getDbChange(), new IntKeyedDbModel.Params(false));
	oneTermRm = new SchemaBufRowModel(oneTermDm.getSchemaBuf());
	
	termInfo.setSwingerMap(fapp.getSwingerMap());
	termInfo.setModel(new RowModelTableModel(oneTermRm,
		new String[] {"Name", "Type", "From", "To (+1)", "Current?"},
		new String[] {"name", "termtypeid", "firstdate", "nextdate", "iscurrent"}));

	duedatesDm = new IntKeyedDbModel(fapp.getSchema("termduedates"),
		"termid", fapp.getDbChange(), new IntKeyedDbModel.Params(false));
	duedates.setModelU(duedatesDm.getSchemaBuf(),
		new String[] {"Status", "Name", "Due Date", "Description"},
		new String[] {"__status__", "name", "duedate", "description"},
		null, fapp.getSwingerMap());
	
	holidaysDm = new IntKeyedDbModel(fapp.getSchema("holidays"),
		"termid", fapp.getDbChange(), new IntKeyedDbModel.Params(false));
	holidays.setModelU(holidaysDm.getSchemaBuf(),
		new String[] {"Status", "First Day", "Last Day", "Description"},
		new String[] {"__status__", "firstday", "lastday", "description"},
		null, fapp.getSwingerMap());
	
	allDm = new MultiDbModel(new DbModel[] {oneTermDm, duedatesDm, holidaysDm});
}

void termChanged(SqlRunner str) 
{
	Integer Termid = (Integer)smod.getTermID();
	allDm.doUpdate(str);
	allDm.setKey(Termid);
	allDm.doSelect(str);
}

	
void all_doSelect(SqlRunner str)
{
	allDm.doSelect(str);
}
	
